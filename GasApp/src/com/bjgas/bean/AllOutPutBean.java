package com.bjgas.bean;

public class AllOutPutBean {
	private String time;
	private float hot;
	private float cold;
	private float elec;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public float getHot() {
		return hot;
	}

	public void setHot(float hot) {
		this.hot = hot;
	}

	public float getCold() {
		return cold;
	}

	public void setCold(float cold) {
		this.cold = cold;
	}

	public float getElec() {
		return elec;
	}

	public void setElec(float elec) {
		this.elec = elec;
	}

}
