package com.bjgas.bean;

public class AllInputBean {
	private String time;
	private float elec;
	private float air;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public float getElec() {
		return elec;
	}

	public void setElec(float elec) {
		this.elec = elec;
	}

	public float getAir() {
		return air;
	}

	public void setAir(float air) {
		this.air = air;
	}

	public float getWater() {
		return water;
	}

	public void setWater(float water) {
		this.water = water;
	}

	private float water;



}
